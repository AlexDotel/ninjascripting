namespace NinjaTrader.NinjaScript.Strategies
{
	public class RSIBB20 : Strategy
	{
		private Bollinger Bollinger1;
		private RSI RSI1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"MEAN REVERSION";
				Name										= "RSIBB20";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				RSIOVERSOLD					= 30;
				RSIOVERBUY					= 70;
				BBPERIODS					= 30;
				BBDEVIATION					= 2;
				RSIMIDLEVEL					= 50;
				RSIPERIODS					= 14;
				COMPRAVAR					= @"COMPRA";
				VENTABAR					= @"VENTA";
				CERRARCOMPRA					= @"CERRARCOMPRA";
				CERRARVENTA					= @"CERRARVENTA";
				BBDEVIATIONGOOD					= 1.5;
			}
			else if (State == State.Configure)
			{
				AddDataSeries("MES MAR24", Data.BarsPeriodType.Tick, 610, Data.MarketDataType.Last);
			}
			else if (State == State.DataLoaded)
			{				
				Bollinger1				= Bollinger(Close, BBDEVIATIONGOOD, Convert.ToInt32(BBPERIODS));
				RSI1				= RSI(Close, Convert.ToInt32(RSIPERIODS), 3);
				Bollinger1.Plots[0].Brush = Brushes.Snow;
				Bollinger1.Plots[1].Brush = Brushes.Transparent;
				Bollinger1.Plots[2].Brush = Brushes.Snow;
				RSI1.Plots[0].Brush = Brushes.DodgerBlue;
				RSI1.Plots[1].Brush = Brushes.Goldenrod;
				AddChartIndicator(Bollinger1);
				AddChartIndicator(RSI1);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 5)
				return;

			 // Set 1
			if ((CrossAbove(GetCurrentAsk(0), Bollinger1.Lower, 1))
				 && (RSI1.Default[5] < RSIOVERSOLD))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), Convert.ToString(COMPRAVAR));
			}
			
			 // Set 2
			if ((RSI1.Default[5] > RSIOVERBUY)
				 && (CrossBelow(Close, Bollinger1.Upper, 1)))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), Convert.ToString(VENTABAR));
			}
			
			 // Set 3
			if ((CrossBelow(Close, Bollinger1.Middle, 1))
				 || (Close[0] > High[1]))
			{
				ExitShort(Convert.ToInt32(DefaultQuantity), Convert.ToString(CERRARVENTA), Convert.ToString(VENTABAR));
			}
			
			 // Set 4
			if ((CrossAbove(Close, Bollinger1.Middle, 1))
				 || (Close[0] < High[1]))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), Convert.ToString(CERRARCOMPRA), Convert.ToString(COMPRAVAR));
			}
			
		}

		#region Properties
