		public int SobreVentaOut
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="InCompra", Description="Nivel de Entrada en compra", Order=5, GroupName="Parameters")]
		public int InCompra
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="InVenta", Description="Nivel de Entrada en venta", Order=6, GroupName="Parameters")]
		public int InVenta
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="StopMid", Description="Decidimos si salir en retroceso.", Order=7, GroupName="Parameters")]
		public bool StopMid
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="StopOutLevel", Description="Nivel de salida de retroceso SL", Order=8, GroupName="Parameters")]
		public int StopOutLevel
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="HacerCompras", Description="Quieres hacer compras?", Order=9, GroupName="Parameters")]
		public bool HacerCompras
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="HacerVentas", Description="Quieres hacer ventas?", Order=10, GroupName="Parameters")]
		public bool HacerVentas
		{ get; set; }
		#endregion

	}
