namespace NinjaTrader.NinjaScript.Strategies
{
	public class RSI15EMA800 : Strategy
	{
		private RSI RSI1;
		private EMA EMA1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Estrategia testeada en pine editor, tomando el RSI como punto de entrada. Validada la tendencia por la emma de 800, funciona bien en 15 y 30 minutos en el SP500.";
				Name										= "RSI15EMA800";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				EmaPeriods					= 800;
				RsiPeriods					= 15;
				SobreCompraOut					= 68;
				SobreVentaOut					= 20;
				InCompra					= 60;
				InVenta					= 51;
				StopMid					= true;
				StopOutLevel					= 50;
				HacerCompras					= true;
				HacerVentas					= true;
			}
			else if (State == State.Configure)
			{
				AddDataSeries("MES MAR24", Data.BarsPeriodType.Minute, 15, Data.MarketDataType.Last);
			}
			else if (State == State.DataLoaded)
			{				
				RSI1				= RSI(Close, Convert.ToInt32(RsiPeriods), 3);
				EMA1				= EMA(Close, Convert.ToInt32(EmaPeriods));
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if ((CrossAbove(RSI1.Avg, InCompra, 1))
				 && (Close[1] > EMA1[0])
				 && (HacerCompras == true))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"COMPRA");
			}
			
			 // Set 2
			if ((Close[1] < EMA1[0])
				 && (CrossBelow(RSI1.Avg, InVenta, 1))
				 && (HacerVentas == true))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"VENTA");
			}
			
			 // Set 3
			if (CrossAbove(RSI1.Avg, SobreCompraOut, 1))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"SALIDACOMPRA", @"COMPRA");
			}
			
			 // Set 4
			if (RSI1.Avg[0] == SobreVentaOut)
			{
				ExitShort(Convert.ToInt32(DefaultQuantity), @"SALIDAVENTA", @"VENTA");
			}
			
			 // Set 5
			if ((StopMid == true)
				 && (CrossAbove(RSI1.Avg, StopOutLevel, 1)))
			{
				ExitShort(Convert.ToInt32(DefaultQuantity), @"SALIDAVENTAMID", @"VENTA");
			}
			
			 // Set 6
			if ((StopMid == true)
				 && (CrossBelow(RSI1.Avg, StopOutLevel, 1)))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"SALIDACOMPRAMID", @"COMPRA");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="EmaPeriods", Description="Valor de la EMA", Order=1, GroupName="Parameters")]
