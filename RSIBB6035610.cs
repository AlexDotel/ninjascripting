			{
				AddDataSeries("MES MAR24", Data.BarsPeriodType.Tick, 610, Data.MarketDataType.Last);
				AddDataSeries("ES MAR24", Data.BarsPeriodType.Tick, 610, Data.MarketDataType.Last);
			}
			else if (State == State.DataLoaded)
			{				
				Bollinger1				= Bollinger(Close, BBDEVIATION, Convert.ToInt32(BBPERIODS));
				RSI1				= RSI(Close, Convert.ToInt32(BBPERIODS), 3);
