				RSI1.Plots[1].Brush = Brushes.Goldenrod;
				AddChartIndicator(Bollinger1);
				AddChartIndicator(RSI1);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 3)
				return;

			 // Set 1
			if ((CrossAbove(Close, Bollinger1.Upper, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Ventas == true))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"VENTA");
			}
			
			 // Set 2
			if ((CrossAbove(Close, Bollinger1.Lower, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Compras == true))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"COMPRA");
			}
			
			 // Set 3
			if (CrossBelow(Low, Bollinger1.Middle, 1))
			{
				ExitShort(Convert.ToInt32(DefaultQuantity), "", @"VENTA");
			}
			
			 // Set 4
			if (CrossAbove(Close, Bollinger1.Middle, 1))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), "", @"COMPRA");
			}
			
			 // Set 5
			if (CrossAbove(Close, Bollinger1.Upper, 1))
			{
				ExitShort(Convert.ToInt32(DefaultQuantity), "", @"VENTA");
			}
			
			 // Set 6
			if (CrossBelow(Close, Bollinger1.Lower, 1))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), "", @"COMPRA");
			}
			
			 // Set 7
			if ((CrossAbove(Close, Bollinger1.Lower, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Compras == true)
				 // RSI
				 && ((SoloBB == false)
				 && (RSI1.Avg[3] <= RSIUPP)))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"COMPRA");
			}
			
			 // Set 8
			if ((CrossAbove(Close, Bollinger1.Upper, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Ventas == true)
				 // RSI
				 && ((SoloBB == false)
				 && (RSI1.Avg[3] >= RSIUPP)))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"VENTA");
			}
			
		}

