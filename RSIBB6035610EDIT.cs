		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if ((CrossBelow(Close, Bollinger1.Upper, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Ventas == true))
			{
				InitialStopLong = MAX1[0];
				BreakEvenBool = false;
				AtrBool = false;
				Print(@"Set 1: " + Convert.ToString(InitialStopShort) + " " + Convert.ToString(Times[0][0]));
				EnterShort(Convert.ToInt32(PositionSize1), @"VENTA1");
				EnterShort(Convert.ToInt32(PositionSize2), @"VENTA2");
			}
			
			 // Set 2
			if ((CrossAbove(Close, Bollinger1.Lower, 1))
				 && (Times[0][0].TimeOfDay >= HoraInicio.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= HoraFin.TimeOfDay)
				 && (Compras == true))
			{
				InitialStopLong = MIN1[0];
				BreakEvenBool = false;
				AtrBool = false;
				Print(@"Set 2: " + Convert.ToString(InitialStopLong) + " " + Convert.ToString(Times[0][0]));
				EnterLong(Convert.ToInt32(PositionSize1), @"COMPRA1");
				EnterLong(Convert.ToInt32(PositionSize2), @"COMPRA2");
			}
			
			 // Set 3
			if ((CrossBelow(Low, Bollinger1.Middle, 1))
				 && (MiddleTP == true))
			{
				ExitShort(Convert.ToInt32(PositionSize1), @"TPVENTA1", @"VENTA1");
				ExitShort(Convert.ToInt32(PositionSize2), @"TPVENTA2", @"VENTA2");
			}
			
			 // Set 4
			if ((CrossAbove(Close, Bollinger1.Middle, 1))
				 && (MiddleTP == true))
			{
				ExitLong(Convert.ToInt32(PositionSize1), @"TPCOMPRA1", @"COMPRA1");
				ExitLong(Convert.ToInt32(PositionSize1), @"TPCOMPRA2", @"COMPRA2");
			}
			
			 // Set 5
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (BreakEvenBool == false)
				 && (AtrBool == false))
			{
				ExitLongStopMarket(Convert.ToInt32(PositionSize1), InitialStopLong, @"STOPCOMPRA1", @"COMPRA1");
				ExitLongStopMarket(Convert.ToInt32(PositionSize2), InitialStopLong, @"STOPCOMPRA2", @"COMPRA2");
				ExitLongLimit(Convert.ToInt32(PositionSize1), (Position.AveragePrice + (Target1Long * TickSize)) , @"TPCOMPRA1", @"COMPRA1");
				ExitLongLimit(Convert.ToInt32(PositionSize2), (Position.AveragePrice + (Target2Long * TickSize)) , @"TPCOMPRA2", @"COMPRA2");
				BreakEvenTargetPriceStore = (Position.AveragePrice + (BreakEvenActivadorLong * TickSize)) ;
				AtrTargetPriceStore = (Position.AveragePrice + (TrailTargetLong * TickSize)) ;
				Print(@"SET 5: BreakEven Target " + Convert.ToString(BreakEvenTargetPriceStore) + " " + Convert.ToString(Times[0][0]));
				Print(@"SET 5: ATR Target " + Convert.ToString(AtrTargetPriceStore) + " " + Convert.ToString(Times[0][0]));
			}
			
		}

